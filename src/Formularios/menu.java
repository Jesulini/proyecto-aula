    class CuentaBancaria {
    private double saldo;

    public CuentaBancaria() {
        this.saldo = 0.0;
    }

    public double consultarSaldo() {
        return saldo;
    }

    public void depositar(double cantidad) {
        saldo += cantidad;
        JOptionPane.showMessageDialog(null, "Se depositaron " + cantidad + " unidades. Saldo actual: " + saldo);
    }

    public void retirar(double cantidad) {
        if (cantidad > saldo) {
            JOptionPane.showMessageDialog(null, "Fondos insuficientes.");
        } else {
            saldo -= cantidad;
            JOptionPane.showMessageDialog(null, "Se retiraron " + cantidad + " unidades. Saldo actual: " + saldo);
        }
    }
}

