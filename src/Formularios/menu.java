    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // Convertir la entrada a un número entero
        String numero1 = texto1.getText();
        int numero = Integer.parseInt(numero1);

        // Evaluar el número y mostrar un mensaje correspondiente
        switch (numero) {
            case 1:
         CuentaBancaria cuenta = new CuentaBancaria();

        while (true) {
            String opcionStr = JOptionPane.showInputDialog(
                "1. Consultar saldo\n" +
                "2. Depositar dinero\n" +
                "3. Retirar dinero\n" +
                "4. Salir\n" +
                "Seleccione una opción:"
            );

            int opcion = Integer.parseInt(opcionStr);

            switch (opcion) {
                case 1:
                    JOptionPane.showMessageDialog(null, "Saldo actual: " + cuenta.consultarSaldo());
                    break;
                case 2:
                    String depositoStr = JOptionPane.showInputDialog("Ingrese la cantidad a depositar:");
                    double deposito = Double.parseDouble(depositoStr);
                    cuenta.depositar(deposito);
                    break;
                case 3:
                    String retiroStr = JOptionPane.showInputDialog("Ingrese la cantidad a retirar:");
                    double retiro = Double.parseDouble(retiroStr);
                    cuenta.retirar(retiro);
                    break;
                case 4:
                    JOptionPane.showMessageDialog(null, "¡Hasta luego!");
                    System.exit(0);
                default:
                    JOptionPane.showMessageDialog(null, "Opción no válida. Inténtelo de nuevo.");      
            break;
            }
        }
            case 2:
            new horarios_y_rutas().setVisible(true);
            break;
            case 3:
            new historial().setVisible(true);
            break;
            case 4:
            setVisible(false);
            break;
            default:
            JOptionPane.showMessageDialog(null, "Número no válido. Por favor, ingrese un número del 1 al 5.");
            break;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

